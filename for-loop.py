# range function 

r = list(range(1, 11))
print(r)

r1 = list(range(15))
print(r1)

r2 = list(range(1,11,3))
print(r2)

r3 = list(range(10,0,-1))
print(r3)

# range(start,end,step)

for i in range(10,0,-1):
    print(i)

# sequnce 

for i in "Kolkata":
    print(i)

